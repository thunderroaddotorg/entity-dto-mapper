package dto;

public abstract class AbstractClassDDTO {

    private long id;

    public AbstractClassDDTO() {
    }

    public AbstractClassDDTO(long id) {
        this.id = id;
    }

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (!(o instanceof AbstractClassDDTO)) return false;

        AbstractClassDDTO that = (AbstractClassDDTO) o;

        return getId() == that.getId();

    }

    @Override
    public int hashCode() {
        return (int) (getId() ^ (getId() >>> 32));
    }
}
